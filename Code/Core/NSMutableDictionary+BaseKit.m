#import "NSMutableDictionary+BaseKit.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation NSMutableDictionary (BaseKit)


////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setObject:(id)anObject forKey:(id)aKey defaultValue:(id)defaultValue {
    id object = (anObject == nil) ? defaultValue : anObject;
    [self setObject:object forKey:aKey];
}

@end
