#import "NSMutableArray+BaseKit.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation NSMutableArray (BaseKit)


////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)addObject:(id)anObject defaultValue:(id)defaultValue {
    id object = (anObject == nil) ? defaultValue : anObject;
    [self addObject:object];
}

@end
